public class Points {
 int x;
 int y;
 int newx;
 int newy;
 public Points(int x, int y, int newx, int newy){
	 this.x = x;
	 this.y = y;
	 this.newx = newx;
	 this.newy = newy;
 }
public int getX() {
	return x;
}
public void setX(int x) {
	this.x = x;
}
public int getY() {
	return y;
}
public void setY(int y) {
	this.y = y;
}
public int getNewx() {
	return newx;
}
public void setNewx(int newx) {
	this.newx = newx;
}
public int getNewy() {
	return newy;
}
public void setNewy(int newy) {
	this.newy = newy;
}
 
}